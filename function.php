<?php
 function multiplication ($x, $y) {

  $mult = $x*$y;
  return $mult;
 }
  

 function risePower ($x, $y) {

  $risePow = $x^$y;
  return $risePow;
 }



 function division ($x, $y) {

  $div = $x/$y;

  return $div;
 }


 function addition ($x, $y) {

  $add = $x+$y;

  return $add;
 }
 
 
 
 
 function subtraction ($x, $y) {

  $sub = $x-$y;
  return $sub;
 }


?>